import axios from 'axios';

import { IWorkoutRoutineGroup } from '@/shared/model/workout-routine-group.model';

const baseApiUrl = 'api/workout-routine-groups';
const baseSearchApiUrl = 'api/_search/workout-routine-groups?query=';

export default class WorkoutRoutineGroupService {
  public search(query): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(`${baseSearchApiUrl}${query}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public find(id: number): Promise<IWorkoutRoutineGroup> {
    return new Promise<IWorkoutRoutineGroup>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IWorkoutRoutineGroup): Promise<IWorkoutRoutineGroup> {
    return new Promise<IWorkoutRoutineGroup>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IWorkoutRoutineGroup): Promise<IWorkoutRoutineGroup> {
    return new Promise<IWorkoutRoutineGroup>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IWorkoutRoutineGroup): Promise<IWorkoutRoutineGroup> {
    return new Promise<IWorkoutRoutineGroup>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
